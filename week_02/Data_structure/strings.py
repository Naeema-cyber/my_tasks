#upper converts all the characters in upper case.
name = "Ada Balogun"
print(name.upper())


#lower converts all characters to lower case

sentence = "python is amazing"
print(sentence.lower())

#title converts all the first characters to upper case
words = "i love python"
print(words.title())

#strip removes whitespace
text = "      Abuja     "
print(text.strip())


#replace
message = "I love Java"
print(message.replace("Java", "python"))

#swapcase
text = "Hello ABEOKUTA"
print(text.swapcase())

